// We can select anything by querySelector 

// class 
let classItem = document.querySelector('.colourname');
console.log(classItem);

let classItem2 = document.querySelectorAll('.colourname');
console.log(classItem2);


// id 
let idItem = document.querySelector('#content');
console.log(idItem);


// Tag Name 
let tagItem = document.querySelector('h1');
console.log(tagItem);

let tagItem2 = document.querySelectorAll('h1');
console.log(tagItem2);
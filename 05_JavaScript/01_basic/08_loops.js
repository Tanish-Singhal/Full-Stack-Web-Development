// TODO: Loops

// For Loop
for (let j = 0; j < 10; j++) {
    console.log(j);
}
console.log();


// While Loop
let i = 0;
while (i < 10) {
    console.log(i);
    i++;
}
console.log();


// Do-while Loop (It execute atlest one time always whether the consdititon is true or false)
let m = 0;
do {
    console.log(m);
    m++;
}while (m < 10);
console.log();